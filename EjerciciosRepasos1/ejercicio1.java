package EjerciciosRepasos1;

/*
1- Escribe un programa en Java que declare variables de diferentes tipos de datos (int,
double, char, boolean, String) y las muestre en pantalla.
 */
public class ejercicio1 {
    public static void main(String[] args) {
        int num = 5;
        double altura = 1.73;
        char op = '+';
        boolean booleano = true;
        String nombre = "ezequiel";

        System.out.println("El nombre es: " + nombre);
        System.out.println("El altura es: " + altura);
        System.out.println("El op es: " + op);
        System.out.println("El boolean es: " + booleano);
        System.out.println("El numero es: " + num);
    }
}
